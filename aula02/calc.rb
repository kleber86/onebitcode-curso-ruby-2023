print "Digite o primeiro numero: "
n1 = gets.chomp.to_i

print "Digite o segundo numero: "
n2 = gets.chomp.to_i

soma = n1 + n2
subtracao = n1 - n2
divisao = n1 / n2
multiplicacao = n1 * n2

puts "A soma de dois numeros inteiros é #{soma}"
puts "A subtração de dois numeros inteiros é #{subtracao}"
puts "A divisão de dois numeros inteiros é #{divisao}"
puts "A multiplicação de dois numeros inteiros é #{multiplicacao}"